 * Задача 1: Сумма чисел
 */

// С использование цикла for
$sum1 = 0;
for ($i = 1; $i <= 25; $i++) {
    $sum1 += $i;
}
echo "1) Сумма: $sum1";

echo "<hr>";

// С использование цикла while
$sum2 = 0;
$i = 1;
while ($i <= 25) {
    $sum2 += $i;
    $i++;
}
echo "2) Сумма: $sum2";
